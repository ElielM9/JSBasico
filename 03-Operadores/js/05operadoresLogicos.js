// Operadores logicos en JS

let a = true; 
let b = false; 

// Operador AND (&&): Verdadero si ambos son verdaderos.
console.log(`A y B son verdaderos?: ${a && b}`);

// Operador OR (||): Verdadero si cualquiera es verdadero.
console.log(`A o B son verdaderos?: ${a || b}`);

// Operador NOT (!): Invierte el valor original.
console.log(`A es verdadero? ${!a}`);
