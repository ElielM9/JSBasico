// Operadores relacionales (Comparadores) en JS

let a = 5; 
let b = 7; 
console.log(`Valor de a: ${a} Valor de b: ${b}`);

// Igual == Es verdadero si dos variables tienen el mismo valor
console.log(`A es igual a B?: ${a == b}`);

// Igual estricto ===: Compara el valor y el tipo de dato. 
console.log(`A es igual que b?: ${a === b}`);

// Diferente !=: Es verdadero si las variables son diferentes.
console.log(`A es diferente a B?: ${a != b}`);

// Diferente estricto !==: Compara el valor y el tipo
console.log(`A es diferente a B?: ${a !== b}`);

// Mayor que >: Es verdadero si la variable es mayor que otra.
console.log(`A es mayor que b?: ${a > b}`);

// Mayor o igual >=: Es verdadero si la variable es mayor o igual que otra.
console.log(`A es mayor o igual a B?: ${a >= b}`);

// Menor que <: Es verdadero si la variable es menor que otra.
console.log(`A es menor que b?: ${a < b}`);

// Menor o igual <=: Es verdadero si la variable es menor o igual que otra.
console.log(`A es menor o igual a B?: ${a <= b}`);
