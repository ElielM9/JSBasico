// Constantes en JS: Es un tipo de variable que necesita ser inicializada con un valor, el valor no puede ser reemplazado a excepción de tipos de datos compuestos.

// Definicion de constantes
const NUMERO = 10;
console.log(NUMERO);

console.log(Math.PI);

const SEGUNDOS_MINUTO = 60;
console.log(`Segundos por minuto: ${SEGUNDOS_MINUTO}`);

// Let vs Const
/*
    Let: Si en el flujo del proyecto el valor va a cambiar.
    Const: Si en el flujo el valor no se modificará. 
*/
