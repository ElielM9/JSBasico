// Operadores Aritmeticos en JS

let a, b, c, d, e, f;

// Suma
a = 3 + 4;
console.log(a);

// Resta
b = 6 - 2;
console.log(b);

// Multiplicacion
c = a * 2;
console.log(c);

// Diviision
d = b / 2.5;
console.log(d);

// Modulo / Residuo
f = 9 % 2;
console.log(f);

// Potencia
e = Math.pow(a, 2);
console.log(e);

// Objeto Math

// PI
a = Math.PI;
console.log(a);

// Redondear
b = Math.round(2.8);
console.log(b);

// Redondear hacia arriba
b = Math.ceil(2.5);
console.log(b);

// Redondear hacia abajo 
b = Math.floor(2.9); 
console.log(b);

// Raiz cuadrada 
c = Math.sqrt(144); 
console.log(c);

// Valor absoluto de un numero 
d = Math.abs(-10); 
console.log(d);

// Minimo 
e = Math.min(a, 3);
console.log(e);

// Maximo 
f = Math.max(b, 15);
console.log(f);

// Aleatorio 
a = Math.random();
console.log(a);

// Aleatorio dentro de rango 
a = Math.floor(Math.random() * 30); 
console.log(a);